package com.listset;

import java.util.Scanner;

public class ScanEx {
	
	
	public static void main(String[] args) {
		
		Scanner s=new Scanner(System.in);
		
		byte age = s.nextByte();
		System.out.println("Employee age is:"+age); 
		
		int empId = s.nextInt();
		System.out.println("Employee mobile no is:"+empId);
		
		short year = s.nextShort();
		System.out.println("employee joining year is"+year);
		
		long mobile = s.nextLong();
		System.out.println("employee mobile is:"+mobile);
		
		float salary = s.nextFloat();
		System.out.println("employee salary is"+salary);
		
		double salary2 = s.nextDouble();
		System.out.println("employee salary2 is:"+salary2);
		
		boolean status = s.nextBoolean();
		System.out.println("employee status is:"+status);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
